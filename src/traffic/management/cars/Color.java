package traffic.management.cars;

public class Color {

	private float r;
	private float b;
	private float g;

	public Color(float r, float b, float g) {
		super();
		this.r = r;
		this.b = b;
		this.g = g;
	}

	public float getR() {
		return r;
	}

	public void setR(float r) {
		this.r = r;
	}

	public float getB() {
		return b;
	}

	public void setB(float b) {
		this.b = b;
	}

	public float getG() {
		return g;
	}

	public void setG(float g) {
		this.g = g;
	}

}
